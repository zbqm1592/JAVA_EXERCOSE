package icehs.science.chapter04;

public class RelationaiOperatorEx {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 3;
		int num3 = 3;
		
		System.out.println( num1 > num2 );
		System.out.println( num1 < num2 );
		System.out.println( num1 >= num2 );
		System.out.println( num1 <= num2 );
		System.out.println( num2 >= num3 );
		System.out.println( num1 == num2 );
		System.out.println( num2 == num3 );
		System.out.println( num1 != num2 );
		System.out.println( num2 != num3 );
		
	}

}
