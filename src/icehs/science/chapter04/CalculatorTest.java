package icehs.science.chapter04;

public class CalculatorTest {

	public static void main(String[] args) {
		int num1 = 20;
		int num2 = 7;
		int add = num1 + num2;
		int sub = num1 - num2;
		int mul = num1 * num2;
		int div = num1 / num2;
		int rem = num1 % num2;
		
		System.out.println("20 쎀 7");
		System.out.println("오 써쎀 : " + (add));
		System.out.println("짋 써쎀 : " + (sub));
		System.out.println("썼 써쎀 : " + (mul));
		System.out.println("씱얋 써쎀 : " + (div));
		System.out.println("씱쟵쵔 찗 써쎀 : " + (rem));

	}

}
